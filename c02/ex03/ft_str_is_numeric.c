int	ft_str_is_numeric(char *str)
{
	int		i;
	int		num;
	char	c;

	i = 0;
	num = 1;
	while (num == 1 && str[i] != '\0')
	{
		c = str[i];
		if ((c < '0') || (c > '9'))
			num = 0;
		i++;
	}
	return (num);
}
